#include <stdio.h>

int main(){
    int A, B, C, D, DIFER;
    scanf("%d", &A);
    scanf("%d", &B);
    scanf("%d", &C);
    scanf("%d", &D);
    DIFER = (A * B - C * D);
    printf("DIFERENCA = %d\n", DIFER);
    return 0;
}

