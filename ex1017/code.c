#include <stdio.h>

int main(){
    int tempo_viagem, velo_media, distancia;
    float litros_necessarios;
    scanf("%d", &tempo_viagem); 
    scanf("%d", &velo_media);
    distancia == tempo_viagem * velo_media;
    litros_necessarios = distancia * 12;
    printf("%3.f\n", litros_necessarios);
    return 0;
}

